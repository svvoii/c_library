#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*str;
	unsigned char	char_c;

	str = (unsigned char *)s;
	char_c = c;
	while (n--)
	{
		if (*str == char_c)
			return (str);
		str++;
	}
	return (NULL);
}
