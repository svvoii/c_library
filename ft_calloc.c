#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	char	*cal;
	char	*tmp;

	cal = malloc(size * nmemb);
	if (!cal)
		return (NULL);
	tmp = cal;
	while (nmemb--)
	{
		*tmp = 0;
		tmp += size;
	}
	return (cal);
}
