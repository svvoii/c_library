#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t	i;
	size_t	j;
	size_t	res;
	size_t	s_len;

	s_len = ft_strlen((char *)src);
	i = 0;
	while (dst && dst[i] && i < size)
		i++;
	res = s_len + i;
	if (size < 1)
		return (res);
	j = i;
	while (src[i - j] && i < size - 1)
	{
		dst[i] = src[i - j];
		i++;
	}
	if (j < size)
		dst[i] = '\0';
	return (res);
}
